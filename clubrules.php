        <li>Driver Restrictions</li>
        <ol>
        	<li>Drivers may double enter an event but only one entry will have standings count for end-of-year Class or Pax trophies. The number of drivers allowed to double enter may be restricted on a first come first served basis.</li>
        	<li>The Eventmaster or Safety Chair may drive the course before an event to estimate the course time or evaluate course safety. If they are competing, the car used must be other than the one used in competition.</li>
        	<li>Drivers who do not promptly report to the Worker Chief for work when required will not be allowed to take any further runs. Additionally, any runs taken prior to that time will be changed to DNS (did not show) and their names will be stricken from all results. The BOD may blacklist a driver from future SASCA events for a pattern of failures to report for worker duty.</li>
        </ol>

        <li>Bumping</li>
        <ul>
        	<li>Bumping (SCCA Rules) is not required.  A driver in a single car class may bump to make a two car class.</li>
        </ul>

        <li>Event Trophies</li>
    	<ul>
    		<li>The winner of each events "Novice" class will receive a trophy at the following monthly meeting.</li>
    	</ul>

        <li>Annual Technical Inspections</li>
        <ol>
        	<li>Members may conduct their own technical inspection at any event during the year by filling out and signing the annual tech inspection roster/checklist.  A member of the Technical Inspection team will show them how to inspect their car.  Upon verification of driver tech inspection proficiency and signing of the Annual Tech Inspection roster, an annual tech inspection sticker will be granted and must be affixed to the vehicle’s windshield.</li>
          <li>Drivers without annual tech inspection stickers must have their cars inspected by the inspection team at every event.</li>
        </ol>

        <li>Run Order</li>
        <ol>
        	<li>Cars are divided into two or three approximately equal-sized groups. Each group makes the expected total number of runs until all cars complete a set of runs. Any additional sets follow in the same order. The Worker Chief may change the order prior to the start of the event with the concurrence of the BOD.</li>
            <li>At the Worker Chief’s discretion, a driver may be granted permission to run out of order.</li>
        </ol>

        <li>Protests</li>
        <ol>
        	<li>Any driver may make Protests. They are limited to the conduct of the event, the classification of a car in the protestant's class, or the Novice status of a driver if they are entered in Novice.</li>
            <li>Protests must be filed with the Board of Directors within 15 minutes of the end of the heat in which the alleged infraction occurred.</li>
            <li>When a formal procedural protest is presented to the Board of Directors, the Board of Directors interrupts the event until the matter is resolved. If a procedural protest is valid, the Board of Directors corrects the procedure and arranges reruns for drivers who were prejudiced by the inappropriate procedure.</li>
            <li>Formal protests of car or driver classification must be filed within 15 minutes after the end of the run heat with the Board of Directors. They must be presented to the Board of Directors in writing and accompanied by a $20.00 protest fee. The Board of Directors then appoints a committee of five to review the protest. Membership typically includes the Safety and Technical Inspection Chairs, a member of the BOD, and another SASCA member. However, no one may be on the committee if its decision affects their status at the event or their end of the year point standings.
            <br/><br/>
						The committee may not require disassembly of a protested car. It may require that compartments be opened (engine compartment or any with access covers), that wheels be removed from the car, and that the car be jacked up to allow inspection of the undercarriage.<br/>
            <br/>
						If a protest of a car or a driver's Novice status is upheld, the Novice or the car driver(s) is disqualified without returning their entry fee(s). The protestor's fee is returned. If the protest is disallowed, the protestor's fee is not returned.
           	</li>
		</ol>

		<li>Optional PAX Modifiers</li>
		<ul>
			<li>A driver may elect to enter only one of the following three classes.</li>
			<ol>
				<li>(N) Novice Class: for drivers who have not placed first through third at any Solo II or autocross type motor sports event. Class standings are based on the PAX times generated from the original SCCA class. Novices ranking first in their class are no longer eligible for the class at future events. R-compound tires are not allowed in Novice class.</li>
				<li>(T) Street Tire: comprised of cars using DOT tires with a wear ratings of 200 and greater.  Class standings are based on the PAX times generated from the original SCCA class.</li>
				<li>(R) Race Tire: New for 2018.  The following SCCA classes shall be combined into the “R” class: <br/>SSP, ASP, BSP, CSP, DSP, ESP, FSP, <br/>SSR, SSM, SM, SMF <br/>CP, DP, EP, FP, GP, XP, <br/>AM, BM, CM, DM, EM, KM.</br> The “R” Class standings are based on the PAX times generated from the original SCCA class. Anyone running in “R” can opt to run in “Tire” class if they are on 200+ TW tires.</li>
				<li>(X) X-Pro: is for advanced drivers who typically go to National events that are wanting to group themselves with others in similar competition.</li>
			</ol>
		</ul>

		<li>Class Championships</li>
		<ol>
			<li>SASCA awards trophies to members with the most points in their SCCA or local PAX class during the year. A member must compete in at least seven of that years’ SASCA autocross events to be eligible for a trophy.  Two lowest-scoring event “drops” are taken into account each season when determining total points for each competitor.</li>
			<li>Points for each event are based on the member's ranking within a class.  First place is awarded six points; second, four; third, three; fourth, two ; and fifth, one.</li>
			<li>Comparing the number of relative standings (1st places, 2nd places, etc.) breaks ties. If a tie persists, two trophies are awarded. Membership status should be active during the last event of the season to be eligible.</li>
		</ol>

		<li>PAX Championship</li>
		<ol>
			<li>SASCA awards trophies to the five entrants with the highest total PAX points accumulated over the year. Only those competing in seven events are eligible for the trophies.</li>
			<li>50 PAX points is awarded to the fastest PAX time at an autocross event. 2nd fastest gets 49 points. Each "next fastest" driver gets 1 point less. All drivers get a minimum of 1 PAX point if they complete the event.  Two lowest-scoring event “drops” are taken into account each season when determining total points for each competitor.</li>
			<li>Comparing the number of relative standings (1st places, 2nd places, etc.) breaks ties. If a tie persists, two trophies are awarded. Membership status should be active during the last event of the season to be eligible.</li>
		</ol>

		<li>Driver of the Year</li>
		<ol>
			<li>The BOD may award a trophy to a member who has demonstrated unusual proficiency or improvement in his or her driving skills during a year. The BOD accepts nominations for the award from any member and may generate nominations at any of its meetings.</li>
            <li>The BOD votes on nominees after the December event. The nominee with the highest number of votes is awarded the trophy. Ties are broken by a run-off between the two candidates with the highest number of votes. If a tie persists, the BOD discusses the candidates and votes again until the tie is broken.</li>
        </ol>

        <li>Novice of the Year</li>
        <ol>
        	<li>The BOD may award a trophy to a member who competes as a Novice and has demonstrated unusual improvement during his or her first year of autocross competition. The BOD accepts nominations for the award from any member and may generate nominations at any of its meetings.</li>
            <li>The BOD votes on nominees after the December event. The nominee with the highest number of votes is awarded the trophy. Ties are broken by a run-off between the two candidates with the highest number of votes. If a tie persists, the BOD discusses the candidates and votes again until the tie is broken.</li>
        </ol>

