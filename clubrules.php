		<li>General Information and Definitions</li>
		<ol>
			<li>The Board of Directors (BOD) is made up of SASCA's elected officers and its immediate past President. If the immediate past President is an officer, the officers may elect a fifth BOD member.</li>
    	<li>The BOD appoints various Chairs to handle specific operations. In addition to Safety (required), Equipment, Membership/Registration, Chief of Workers, Timing and Scoring, Novice Chief, and Technical Inspection Chairs are typically appointed.</li>
			<li>Unless otherwise stated below, autocross events are governed by the Sports Car Club of America's (SCCA) SOLO II RULES. Since the new rulebook is not available on 1 January, the old rules are used until it is available. The Board of Directors ensures that copies of the current rules and this document are available on the web site.</li>
			<li>SCCA rules may be modified by statements in the FASTRACK Releases.  A driver is responsible for providing the appropriate FASTRACK if necessary to resolve disputes.</li>
        </ol>

        <li>Safety Steward</li>
        <ol>
			<li>The Safety Steward and Board of Directors are responsible for ensuring that courses are appropriate and safe. If they can't attend an event, the BOD appoints a temporary Safety Steward.</li>
			<li>Eventmasters must review their courses with the Safety Steward and Board of Directors before setting up the course.</li>
			<li>The Safety Chair or Board of Directors may change a course at any time if driver or vehicle safety is compromised. If they do so, drivers who have completed any runs get reruns. With the concurrence of the BOD members present, they may suspend or terminate an event if factors other than the course (e.g., weather) compromise driver safety.</li>
			<li>Anyone at an event may bring a safety issue to the Chairs' attention.</li>
        </ol>

