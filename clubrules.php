        <li>Annual Car Numbers and Technical Inspections</li>
        <ol>
        	<li>SASCA members may apply for a number to use for the calendar year by requesting one during registration.  A member of Technical Inspection team will the show them how to inspect their car.</li>
            <li>Members with annual numbers may conduct their own technical inspection at any event during the  year by filling out and signing the inspection check list on their entry form.</li>
            <li>Drivers without annual numbers must have their cars inspected by the inspection team.</li>
            <li>The Registration Chair may release an annual number to another driver when it becomes apparent that the driver assigned the number will not compete at an event.</li>
        </ol>

        <li>Run Order</li>
        <ol>
        	<li>Cars are divided into two or three approximately equal sized groups. Each group makes Â½ of the expected total number of runs until all cars complete a set of runs. Any additional sets follow in the same order. The Eventmaster may change the order prior to the day of the event with the concurrence of the BOD.</li>
            <li>At the Eventmaster's discretio, a driver may be granted permission to run out of order.</li>
        </ol>

        <li>Protests</li>
        <ol>
        	<li>Any driver may make Protests.  They are limited to the conduct of the event, the classification of a car in the protestant's class, or the Novice status of a driver if they are entered in Novice.</li>
            <li>Protests must be discussed with the Eventmaster within 15 minutes of the alleged infraction in procedure or the first run of the allegedly misclassified car or driver.  If the matter is not resolved, a formal protest may be filed with the Eventmaster.</li>
            <li>When a formal procedural protest is presented to the Eventmaster, the Eventmaster interrupts the event until the matter is resolved.  If a procedural protest is valid, the Eventmaster corrects the procedure and arranges reruns for drivers who were prejudiced by the inappropriate procedure.</li>
            <li>Formal protests of car or driver classification must be filed within 15 minutes after the end of the discussion with the Eventmaster.  They must be presented to the Eventmaster in writing and accompanied by a $20.00 protest fee.  The Eventmaster then appoints a committee of five to review the protest.  Membership typically includes the Autocross, Safety, and Technical Inspection Chairs, a member of the BOD, and another SASCA member.  However, no one may be on the committee if its decision affects their status at the event or their end of the year point standings.
            <br/><br/>
            The committee may not require disassembly of a protested car.  It may require that compartments be opened (engine compartment or any with access covers), that wheels be removed from the car, and that the car be jacked up to allow inspection of the undercarriage.<br/>
            <br/>
            If a protest of a car or a driver's Novice status is upheld, the Novice or the car driver(s) is disqualified without returning their entry fee(s).  The protestor's fee is returned.  If the protest is disallowed, the protestor's fee is not returned.
           	</li>
		</ol>

		<li>Optional Classes</li>
		<ul>
			<li>A driver may elect to enter only one of the following three classes.</li>
			<ol>
				<li>Novice Class is for drivers who have not placed first though third at any Solo II or autocross type motor sports event.  Class standings are based on the PAX times generated from the original SCCA class.  Novices ranking first in their class on two occasions are no longer eligible for the class. R compound tires are not allowed in Novice class.</li>
				<li>Street Tire is comprised of cars using DOT tires with a wear ratings of 150 and greater.  Class standings are based on the PAX times generated from the original SCCA class.</li>
			</ol>
		</ul>

		<li>Class Championships</li>
		<ol>
			<li>SASCA awards trophies to members with the most points in their SCCA or Street Modified class during the year.  A member must compete in at least two thirds (2/3s) of that years SASCA autocross events to be eligible for a trophy.</li>
			<li>Points for each event are based on the member's ranking within a class.  First place is awarded six points; second, four; third, three; fourth, two ; and fifth, one.</li>
			<li>Comparing the number of relative standings (1st places, 2nd places, etc.) breaks ties.  If a tie persists, two trophies are awarded.</li>
		</ol>

		<li>PAX Championship</li>
		<ol>
			<li>SASCA awards trophies to the five entrants with the highest total PAX points accumulated over the year.  Only those competing in eight events are eligible for the trophies.</li>
			<li>50 PAX points is awarded to the fastest PAX time at an autocross event.  2nd fastest gets 49 points.  Each "next fastest" driver gets 1 point less.  All drivers get a minumum of 1 PAX point if they complete the event.</li>
			<li>Comparing the number of relative standings (1st places, 2nd places, etc.) breaks ties.  If a tie persists, two trophies are awarded.</li>
		</ol>

		<li>Driver of the Year</li>
		<ol>
			<li>The BOD may award a trophy to a member who has demonstrated unusual proficiency or improvement in his or her driving skills during a year.  The BOD accepts nominations for the award from any member and may generate nominations at any of its meetings.</li>
            <li>The BOD votes on nominees after the November event.  The nominee with the highest number of votes is awarded the trophy.  Ties are broken by a run off between the two candidates with the highest number of votes.  If a tie persists, the BOD discusses the candidates and votes again until the tie is broken.</li>
        </ol>

        <li>Novice of the Year</li>
        <ol>
        	<li>The BOD may award a trophy to a member who competes as a Novice and has demonstrated unusual improvement during his or her first year of autocross competition.  The BOD accepts nominations for the award from any member and may generate nominations at any of its meetings.</li>
            <li>The BOD votes on nominees after the November event.  The nominee with the highest number of votes is awarded the trophy.  Ties are broken by a run off between the two candidates with the highest number of votes.  If a tie persists, the BOD discusses the candidates and votes again until the tie is broken.</li>
        </ol>

