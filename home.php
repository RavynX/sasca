			<h1>Welcome to SASCA!</h1>
			<p style="margin-top: 8px;">Thanks for swinging on by!  We're your local autocross club for the San Antonio, TX area.  Interested in seeing what we're about?  Come to our next event or one of our monthly meetings and introduce yourself.  It's free to spectate and many people offer their passenger seat for ride-alongs.</p>
			<br/>

			<h1>Spokes/SASCA Mini-Divisional - June 25/26</h1>
			<p class="date">June 6, 2016</p>
			<p>Spokes and SASCA will be hosting autocrosses on back to back days. Spokes is taking this opportunity to setup a divisional event that will mimic national level SCCA events.</p>
			<p>For those unfamiliar with national level events, the competition spans 2 days of racing and your best times from each day are combined for competition.</p>
			<p>We are still working on some details but here is what we have so far:
				<ul style="list-style-type:circle; padding-left: 30px;">
					<li>The number of runs at each event will remain unchanged. (This gets decided based on how quickly the first heat progresses. We typically get 4 - 6 runs per event.)</li>
					<li>The number of runs that will factor into the weekend long divisional competition is TBD.</li>
					<li>There will be prizes for class winners of the weekend long competition as well as prizes for other notable performances.</li>
					<li>We are planning to have a social event on the evening of Saturday, June 25th.</li>
				</ul></p>
				<p>SASCA will be hosting on Saturday, June 25 @ River City Community Church</p>
				<p>Spokes will be hosting on Sunday, June 26 @ San Antonio Raceway</p>
			</p>
