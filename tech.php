<h1>Tech Inspection Checklist</h1>
<div id="tech">
	<p class="top">This is a list of items that will be checked in order to pass tech inspection for SASCA autocross.  If you have a SASCA annual tech inspection sticker you must perform these checks every event yourself.</p>

	<p class="heading">Tires</p>
	<p>No cords showing on any of the 4 tires.</p>

	<p class="heading">Wheels and Lugs</p>
	<p>All lugs in place, suspension is tight, and no play in the wheel bearings.</p>

	<p class="heading">Fluids</p>
	<p>No fluid leaks under the hood or under the car.</p>

	<p class="heading">Battery</p>
	<p>Battery must be secure.</p>

	<p class="heading">Under hood</p>
	<p>Leaks, battery, air cleaner type, modifications (for classing), loose items.</p>

	<p class="heading">Pedals</p>
	<p>Both brake and accelerator pedals are working properly (firm brake, accelerator returns smoothly).</p>

	<p class="heading">Loose items</p>
	<p>All loose items should be removed from the trunk and the vehicle cabin, including floor mats.</p>

	<p class="heading">Seat Belts</p>
	<p>Some type of functional seat belt must be used.</p>

	<p class="heading">Seats</p>
	<p>Seats must be secure.</p>

	<p class="heading">Numbers and Class Letters</p>

	<ol>
		<li>Numbers should be 2 digits except for 2 driver cars, where one driver will use a 2 digit number, and the second driver will use "1" followed by the same number (e.g. 23 and 123).</li>
		<li>Numbers and class letters should contrast with the car color and be large enough for course workers to easily see them.</li>
	    <li>All numbers and class letters should be on the vehicle before inspection.</li>
	    <li>Numbers should be 8+" and class letters should be 4+", but the numbers should be noticeably larger than the class letters.</li>
	    <li>Numbers should be on body panels. No numbers on windows.</li>
	    <li>Special classing letters such as Novice (N) or Double (D) should precede the classing letters.
	</ol>
</div>
