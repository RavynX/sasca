<h1>Doubles</h1>
<b>Registration</b>
<ol id="doubles_require">
  <li>Must be pre-registered with your Open class for the event on Axware.</li>
  <li>Must check-in on event day.  The first 15 to check-in will make it on the Doubles list.</li>
  <li>If you were not pre-registered, check back when registration is about to close to see if there are any open slots.</li>
</ol>

<b>Work Assignment</b>
<p>You will need to work once for each of your entries (Doubles = 2 entries, which means you work twice).<br/>
  There are several scenarios that affect how doubles drivers run/work:</p>

<div class="inset">
  <p><u>Three Heat Events</u></p>
  <p>If the event has three heats, you must work one work assignment in the morning or during the event</p>
  <p><b>-AND-</b></p>
  <p>You must pack the trailer at the end of the event. <b>(No exceptions)</b></p>
  <br/>
</div>

<div class="inset">
  <p><u>Two Heat Events</u></p>
  <p>If the event has only two heats, you must:</p>
    <ol id="doubles_work">
      <li>Complete all of your Open and Double runs in the one heat that you don't work.</li>
      <li>Work one assignment during the event</li>
    </ol>
    <b>-AND-</b><br/>
    You must pack the trailer at the end of the event. <b>(No exceptions)</b><br/>
</div>
<br/>

<b>Rules</b>
<ol id="doubles_rules">
  <li>You must run your Open class first, either in 1st or 2nd heat.</li>
  <li>Your Double runs will always occur in the last heat.</li>
</ol>

<b>Disclaimers</b>
<p>Running Doubles does <u>NOT</u> inherently grant you rights to morning work. Morning work assignments are still assigned based upon a separate first come/first served basis.
SASCA BOD determines if Doubles will be offered after online registration closes before the event date. SASCA BOD reserves the right to cancel doubles entries for any reason. Upon cancelation on event day, and if attendees have already paid for doubles entries, refunds will be given for Doubles entries.</p>


